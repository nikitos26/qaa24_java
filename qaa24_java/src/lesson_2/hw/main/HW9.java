package lesson_2.hw.main;

//        Необходимо вывести на экран числа от 5 до 1.
//        При решении используйте операцию декремента (--)

public class HW9 {
    public static void main(String[] arg) {
        for (int i = 5; i >= 1; i--) {
            System.out.print(" " + i);
        }
    }
}
