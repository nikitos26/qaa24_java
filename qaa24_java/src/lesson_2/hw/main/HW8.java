package lesson_2.hw.main;

//        При помощи цикла for вывести на экран нечетные числа от 1 до 99.
//        При решении используйте операцию инкремента (++)

public class HW8 {
    public static void main(String[] arg) {
        for (int i = 1; i <= 99; i += 2) {
            System.out.print(" " + i);
        }
    }
}
